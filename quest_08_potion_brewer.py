# Using arithmetic operators (* for multiplication and + for addition).
# I am calculating the individual costs first, then summing them for the total.
scales_needed = 3
scale_price = 10
tears_needed = 5
tear_price = 3

total_cost = (scales_needed * scale_price) + (tears_needed * tear_price)

print(f"The Potion Brewer says: Your total comes to {total_cost} gold pieces.")