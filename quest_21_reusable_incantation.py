# Level 5 begins: Functions (def) allow us to group code for reuse.
# This keeps our code "DRY" (Don't Repeat Yourself).
def greet_adventurer():
    # This message is packaged inside the function.
    print("Welcome, brave adventurer, to the Alchemist's Lab!")

# Calling the function three times as requested.
greet_adventurer()
greet_adventurer()
greet_adventurer()
